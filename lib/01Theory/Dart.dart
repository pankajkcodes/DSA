/*
    DART DATA TYPES
│
├── 1. SINGLE VALUE TYPES
│   │
│   ├── int
│   │     → Whole Numbers
│   │     → 10, -5, 100
│   │
│   ├── double
│   │     → Decimal Numbers
│   │     → 10.5, 99.99
│   │
│   ├── num
│   │     → Parent of int & double
│   │
│   ├── String
│   │     → Text
│   │     → "Hello"
│   │
│   └── bool
│         → true / false
│
│
├── 2. MULTIPLE VALUE TYPES (COLLECTIONS)
│   │
│   ├── List
│   │     → Ordered Collection
│   │     → [1,2,3]
│   │     → Duplicate Allowed
│   │
│   ├── Set
│   │     → Unique Values Only
│   │     → {1,2,3}
│   │     → Duplicate NOT Allowed
│   │
│   └── Map
│         → Key : Value Pair
│         → {"name":"Pankaj"}
│
│
├── 3. FLEXIBLE TYPES
│   │
│   ├── dynamic
│   │     → Can store ANY type
│   │     → Unsafe sometimes
│   │
│   └── Object
│         → Parent of all objects
│
│
├── 4. NULL SAFETY TYPES
│   │
│   ├── String
│   │     → Cannot be null
│   │
│   ├── String?
│   │     → Can be null
│   │
│   ├── ?
│   │     → Safe access
│   │
│   ├── !
│   │     → Force not null
│   │
│   └── ??
│         → Default value if null
│
│
├── 5. VARIABLE DECLARATION TYPES
│   │
│   ├── var
│   │     → Dart auto-detects type
│   │
│   ├── final
│   │     → Assign once
│   │
│   └── const
│         → Compile-time constant
│
│
├── 6. FUNCTION TYPE
│   │
│   └── Function
│         → Store functions in variables
│
│
└── 7. RARE TYPES
    │
    ├── Runes
    │     → Unicode / Emoji
    │
    └── Symbol
          → Identifier symbols

 */